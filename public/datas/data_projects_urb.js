      <p>O principal desafio da equipe foi estabelecer uma conexão espacial qualificada entre os núcleos histórico e comercial do município, separados pela linha férrea Santos–Jundiaí. A intervenção, contudo, extrapola a simples resolução da descontinuidade do tecido urbano, assumindo caráter mais amplo de reestruturação do espaço público central.</p>
      <br>

      <p>A transposição da ferrovia já havia sido objeto de reflexão no Plano de Renovação Urbana de 1975, elaborado por Vilanova Artigas. Parcialmente implantado, o plano previa não apenas passarelas de ligação entre as áreas histórica e comercial, mas também a construção de um paço municipal e de um parque capaz de articular diferentes equipamentos urbanos.</p>
      <br>

      <p>Com exceção de uma passarela e do edifício da prefeitura, o restante do plano não chegou a ser executado. Nesse contexto, surgiu a proposta da esplanada elevada como solução contemporânea de conexão entre as áreas centrais.</p>
      <br>

      <p>O projeto contempla ainda a implantação de uma biblioteca, a construção de um ginásio poliesportivo e a relocação do terminal rodoviário, que foi redesenhado e será implantado sob parte da esplanada. Os arquitetos também são autores do projeto de cobertura da estação elevada da CPTM (Companhia Paulista de Trens Metropolitanos), inserida no plano de modernização das estações ferroviárias.</p>
